class controller_engineering_resources_replacedinfo extends controller_base_action {

	function __construct() {
		$this->objName = "replacedinfo";
		$this->objPath = "engineering_resources";
		parent::__construct ();
	 }
    
	/**
	 * 跳转到设备管理-可替换设备管理-可替换物料列表
	 */
    function c_page() {
      $this->view('list');
    }
    
   /**
	 * 跳转到新增设备管理-可替换设备管理-可替换物料页面
	 */
	function c_toAdd() {
     $this->view ( 'add' );
   }
   
   /**
	 * 跳转到编辑设备管理-可替换设备管理-可替换物料页面
	 */
	function c_toEdit() {
   	$this->permCheck (); //安全校验
		$obj = $this->service->get_d ( $_GET ['id'] );
		foreach ( $obj as $key => $val ) {
			$this->assign ( $key, $val );
		}  
      $this->view ( 'edit');
   }
   
   /**
	 * 跳转到查看设备管理-可替换设备管理-可替换物料页面
	 */
	function c_toView() {
      $this->permCheck (); //安全校验
		$obj = $this->service->get_d ( $_GET ['id'] );
		foreach ( $obj as $key => $val ) {
			$this->assign ( $key, $val );
		}
      $this->view ( 'view' );
   }
 }
