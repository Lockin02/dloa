class controller_hr_baseinfo_trialplantem extends controller_base_action {

	function __construct() {
		$this->objName = "trialplantem";
		$this->objPath = "hr_baseinfo";
		parent :: __construct();
	}

	/**
	 * 跳转到员工试用培训计划模板列表
	 */
	function c_page() {
		$this->view('list');
	}

	/**
	 * 跳转到新增员工试用培训计划模板页面
	 */
	function c_toAdd() {
		$this->view('add');
	}

	/**
	 * 跳转到编辑员工试用培训计划模板页面
	 */
	function c_toEdit() {
		$this->permCheck(); //安全校验
		$obj = $this->service->get_d($_GET['id']);
		foreach ($obj as $key => $val) {
			$this->assign($key, $val);
		}
		$this->view('edit');
	}

	/**
	 * 跳转到查看员工试用培训计划模板页面
	 */
	function c_toView() {
		$this->permCheck(); //安全校验
		$obj = $this->service->get_d($_GET['id']);
		foreach ($obj as $key => $val) {
			$this->assign($key, $val);
		}
		$this->view('view');
	}
}
